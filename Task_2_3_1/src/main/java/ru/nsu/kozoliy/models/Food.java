package ru.nsu.kozoliy.models;

public class Food {
    int x;
    int y;

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public Food(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
